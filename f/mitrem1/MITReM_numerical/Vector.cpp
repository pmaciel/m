//---------------------------------------------------------------------------
Vector::Vector (unsigned nComponents_)
	: nComponents(nComponents_)
{	
	components = new double[nComponents];
}
//---------------------------------------------------------------------------
Vector::~Vector ()
{
	delete[] components;
}
//---------------------------------------------------------------------------
void Vector::setComponents(unsigned c, double value)
{
	if (c < nComponents)
	{
		components[c] = value;
	}
	else
	{
		// error message
	}
}
