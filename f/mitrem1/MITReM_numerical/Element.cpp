//---------------------------------------------------------------------------
Element::Element (unsigned nNodes_)
	: nNodes(nNodes_)
{	
	nodes = new unsigned[nNodes];
}
//---------------------------------------------------------------------------
Element::~Element ()
{
	delete[] nodes;
}
//---------------------------------------------------------------------------
void Element::setNodes(unsigned m, unsigned value)
{
	if (m < nNodes)
	{
		nodes[m] = value;
	}
	else
	{
		// error message
	}
}
